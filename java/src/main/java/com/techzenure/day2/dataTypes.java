package com.techzenure.day2;

public class dataTypes {
    public static void main(String args[]) {
		byte bte = 122;
		short bill = 2164;
		int number = 786;
		long salary = 108747;
		float gpa = 2.2f;
		double asset = 8000.0;
		char grade = 'A';
		String str = "hello";
		boolean valid = true;
		System.out.println("Byte : "+ bte);
		System.out.println("int : "+ number);
		System.out.println("Short : "+ bill);
		System.out.println("Long : "+ salary);
		System.out.println("Float : "+ gpa);
		System.out.println("Double : "+ asset);
		System.out.println("Char : "+ grade);
		System.out.println("String : "+ str);
		System.out.println("Boolean : "+ valid);

	}
}
